# Operasi yang dapat dilakukan dengan meringkas

a = 5 # adalah assignment
print("Nilai a = ", a)

a += 1 # artinya a = a + 1
print("Nilai a += 1, nilai a menjadi ", a)

a -= 1 # artinya a = a - 1
print("Nilai a -= 1, nilai a menjadi ", a)

a *= 1 # artinya a = a * 1
print("Nilai a *= 1, nilai a menjadi ", a)

a /= 1 # artinya a = a / 1
print("Nilai a /= 1, nilai a menjadi ", a)

# Modulus dan Floor Division
b = 10
print("Nilai b = ", b)

b %= 3 # artinya b = b % 3
print("Nilai b %= 3, nilai b menjadi ", b)

b = 10
print("Nilai b = ", b)

b //= 3 # artinya b = b // 3
print("Nilai b //= 3, nilai b menjadi ", b)

# Pangkat (Eksponen)
a = 5 
print("Nilai a = ", a)
a **= 3
print("Nilai a **= 3, nilai a menjadi = ", a)

# Bagaimana jika Operasi menjadi Logika?
# OR, AND

# OR (|)
c = True
print("Nilai c = ", c)
c |= False # artinya c = c or false
print("Nilai c |= False, nilai c menjadi ", c)

# AND (&)
c = False
print("Nilai c = ", c)
c &= False # artinya c = c and false
print("Nilai c &= False, nilai c menjadi ", c)